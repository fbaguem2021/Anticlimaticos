                <h2>Cambio climático</h2>
                <p>El <span style="font-weight:bold">cambio climático</span> se refiere a los cambios a largo plazo de las temperaturas y los patrones climáticos. Estos cambios pueden ser naturales, como las variaciones del ciclo solar. Pero desde el siglo XIX, las actividades humanas han sido el principal motor del cambio climático, principalmente debido a la quema de combustibles fósiles como el carbón, el petróleo y el gas. </p>
                <p>La quema de combustibles fósiles genera emisiones de gases de efecto invernadero que actúan como una manta que envuelve la Tierra, atrapando el calor del sol y elevando las temperaturas.</p>

                <p> Entre los ejemplos de emisiones de gases de efecto invernadero que provocan el cambio climático se encuentran el dióxido de carbono y el metano. Éstos proceden del uso de gasolina para conducir un coche o de carbón para calentar un edificio, por ejemplo. El desmonte de tierras y bosques también puede liberar dióxido de carbono. Los vertederos de basura son una fuente importante de emisiones de metano. La energía, la industria, el transporte, los edificios, la agricultura y el uso del suelo son algunos de los principales emisores.</p>

