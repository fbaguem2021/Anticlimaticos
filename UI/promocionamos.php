                <h2>Objetivos</h2>
                <p>Muchas soluciones para el cambio climático pueden aportar beneficios económicos al tiempo que mejoran nuestras vidas y protegen el medio ambiente. También contamos con marcos y acuerdos globales para guiar el progreso, como los Objetivos de Desarrollo Sostenible, la Convención Marco de las Naciones Unidas sobre el Cambio Climático y el Acuerdo de París. Hay tres grandes categorías de acción: reducir las emisiones, adaptarse a los impactos climáticos y financiar los ajustes necesarios.</p>
                <p> El cambio de los sistemas energéticos de los combustibles fósiles a las energías renovables, como la solar o la eólica, reducirá las emisiones que provocan el cambio climático. Pero hay que empezar ya. Aunque una coalición cada vez mayor de países se compromete a lograr un nivel neto de emisiones cero para 2050, aproximadamente la mitad de las reducciones de emisiones deben estar en marcha para 2030 para mantener el calentamiento por debajo de 1,5 °C. La producción de combustibles fósiles debe disminuir aproximadamente un 6% al año entre 2020 y 2030.</p>
